r12=x(2);
r13=x(3);
r21=x(4);
r22=x(5);
r23=x(6);
r31=x(7);
r32=x(8);
r33=x(9);
t1=x(10);
t2=x(11);
t3=x(12);


 n1=[0;0.62260056;0.78253980];                   %初始位置光平面1法向量
