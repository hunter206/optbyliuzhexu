 d=1.13085689;
 

x0=[0;0;0];
x1=[-5.637346;97.293991;184.52958];
x2=[83.872458;79.44437;308.470164];
x3=[-110.227859;-44.626329;550.671631];
x4=[-62.879544;-199.383353;265.965473];
x5=[-104.838603;-106.260285;149.351819];   %各点坐标


 R=[r11 r12 r13;r21 r22 r23;r31 r32 r33];
 T=[t1; t2; t3];
 xw0=R*x0+T;
 xw1=R*x1+T;
 xw2=R*x2+T;
 xw3=R*x3+T;
 xw4=R*x4+T;
 xw5=R*x5+T;
 
 
t01=0.238677241;
t02=0.465954214;
t11=0.235873606;
t12=0.474396034;
t21=0.232219456;
t22=0.479312124;
t31=0.217756889;
t32=0.482104059;
t41=0.221958069;
t42=0.469669584;
t51=0.228686998;
t52=0.467421172;




 syms a01
 syms a02
 a01= -1*t01 * 2*pi;
 a02=  -1*t02 * 2*pi;
 R01=[cos(a01) -sin(a01) 0;sin(a01) cos(a01) 0;0 0 1];
 R02=[cos(a02) -sin(a02) 0;sin(a02) cos(a02) 0;0 0 1];
 n01=R01*n1;                                             %光平面1扫过点0时的法矢量
